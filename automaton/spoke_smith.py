def generate_spoke_file(spec: dict) -> Path:
    name = spec["name"]
    out_path = ROOT / f"build_{name}.py"
    rationale = (spec.get("rationale") or "").strip()
    if "\n" in rationale:
        rationale = "\n".join(line.strip() for line in rationale.splitlines() if line.strip())
    code = SPOKE_TEMPLATE.format(
        name=name,
        title=spec.get("title", name),
        ts=dt.datetime.now(dt.timezone.utc).isoformat(),
        rationale=rationale[:400],
        ttl=int(spec.get("source", {}).get("ttl_hours", 24)),
        url=spec.get("source", {}).get("url", "ABOUT:BLANK"),
        fields_required=spec.get("fields_required", []),
        output_schema=spec.get("output_schema", {}),
    )
    out_path.write_text(code, encoding="utf-8")
    return out_path


def trial_run(name: str) -> tuple[bool, str]:
    script = ROOT / f"build_{name}.py"
    if not script.exists():
        return False, "script does not exist"
    try:
        result = subprocess.run(
            ["python3", str(script)],
            capture_output=True, text=True, timeout=60,
            cwd=str(ROOT),
        )
        return (result.returncode == 0,
                f"rc={result.returncode}  stdout={result.stdout[-200:]}")
    except subprocess.TimeoutExpired:
        return False, "trial run timeout (>60s)"
    except Exception as e:  # noqa: BLE001
        return False, f"{type(e).__name__}: {e}"


def find_next_eligible(spokes: list) -> dict | None:
    queued = [s for s in spokes if s.get("state") == "queued"]
    if not queued:
        return None
    queued.sort(key=lambda s: s.get("priority", 99))
    return queued[0]


def transition(state_data: dict, name: str, new_state: str) -> None:
    for s in state_data.get("spokes", []):
        if s.get("name") == name:
            s["state"] = new_state
            s["last_transition_utc"] = dt.datetime.now(dt.timezone.utc).isoformat()


def main():
    p = argparse.ArgumentParser()
    p.add_argument("--next", action="store_true", help="ship highest-priority queued spoke")
    p.add_argument("--name", help="ship a specific spoke by name (overrides --next)")
    p.add_argument("--dry-run", action="store_true", help="don't write or transition")
    args = p.parse_args()

    if not QUEUE_PATH.exists():
        log(f"ERROR: queue file missing: {QUEUE_PATH}")
        return 2
    state_data = load_yaml(QUEUE_PATH)
    spokes = state_data.get("spokes", [])

    if args.name:
        target = next((s for s in spokes if s.get("name") == args.name), None)
        if not target:
            log(f"ERROR: --name {args.name} not in queue")
            return 2
    else:
        target = find_next_eligible(spokes)
        if not target:
            log("no eligible queued spokes")
            return 1

    name = target["name"]
    log(f"target spoke: {name}  priority={target.get('priority')}  state={target.get('state')}")

    if args.dry_run:
        log("DRY RUN — would generate build_{}.py".format(name))
        return 0

    out_path = generate_spoke_file(target)
    log(f"generated {out_path.name}  ({out_path.stat().st_size} bytes)")

    transition(state_data, name, "in_progress")
    state_data["last_run_utc"] = dt.datetime.now(dt.timezone.utc).isoformat()
    dump_yaml(state_data, QUEUE_PATH)

    ok, msg = trial_run(name)
    log(f"trial run: ok={ok}  detail={msg}")

    if not ok:
        log("trial failed — leaving spoke in_progress for manual review")
        return 3

    log(f"DONE: {name} → in_progress (parser stub awaits human/agent fill-in)")
    log(f"NEXT: implement parse() in {out_path.name}, then run spoke_audit.py --name {name}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
