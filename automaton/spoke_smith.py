def log(msg: str) -> None:
    ts = dt.datetime.now(dt.timezone.utc).isoformat()
    line = f"[{ts}] {msg}\n"
    with LOG_PATH.open("a", encoding="utf-8") as fh:
        fh.write(line)
    sys.stdout.write(line)


def load_yaml(path: Path) -> dict:
    """Minimal YAML loader sufficient for our spec shape (no anchors, no flow)."""
    try:
        import yaml  # type: ignore
        return yaml.safe_load(path.read_text(encoding="utf-8")) or {}
    except ImportError:
        log("WARN: PyYAML not installed — using fallback parser (limited)")
        return _fallback_yaml(path.read_text(encoding="utf-8"))


def dump_yaml(data: dict, path: Path) -> None:
    try:
        import yaml  # type: ignore
        path.write_text(yaml.safe_dump(data, sort_keys=False, default_flow_style=False),
                        encoding="utf-8")
    except ImportError:
        # Fallback: round-trip via json (loses comments — acceptable for state file)
        path.write_text(json.dumps(data, indent=2), encoding="utf-8")


def _fallback_yaml(text: str) -> dict:
    """Tiny fallback parser — only handles the subset our spec uses.
    Strongly recommended: pip install pyyaml. This is a backup."""
    out: dict = {"spokes": []}
    cur: dict | None = None
    indent_stack: list[tuple[int, dict | list]] = [(0, out)]
    for raw in text.splitlines():
        if not raw.strip() or raw.lstrip().startswith("#"):
            continue
        # Very minimal: detect "  - name:" entries and "    key: value" lines
        m = re.match(r"^(\s*)-\s+(\w+):\s*(.*)$", raw)
        if m:
            depth = len(m.group(1))
            cur = {m.group(2): m.group(3).strip().strip('"')}
            out.setdefault("spokes", []).append(cur)
            continue
        m = re.match(r"^(\s*)(\w+):\s*(.*)$", raw)
        if m and cur is not None:
            cur[m.group(2)] = m.group(3).strip().strip('"')
    return out


